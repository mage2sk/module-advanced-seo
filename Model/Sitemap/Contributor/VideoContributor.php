<?php
declare(strict_types=1);

namespace Panth\AdvancedSEO\Model\Sitemap\Contributor;

use Magento\Framework\App\Config\ScopeConfigInterface;
use Magento\Framework\App\ResourceConnection;
use Magento\Store\Model\ScopeInterface;
use Magento\Store\Model\StoreManagerInterface;
use Panth\AdvancedSEO\Api\SitemapContributorInterface;

/**
 * Streams product URLs that have external video entries in the media gallery.
 * Each emitted entry includes a `video` key with video:video child data
 * (title, description, thumbnail_loc, content_loc) for the sitemap XML.
 *
 * Uses unbuffered PDO query to keep memory bounded on large catalogues.
 */
class VideoContributor implements SitemapContributorInterface
{
    private const XML_INCLUDE_VIDEO = 'panth_seo/sitemap/include_video';

    public function __construct(
        private readonly ResourceConnection $resource,
        private readonly StoreManagerInterface $storeManager,
        private readonly ScopeConfigInterface $scopeConfig
    ) {
    }

    public function getCode(): string
    {
        return 'video';
    }

    public function getUrls(int $storeId, array $config = []): \Generator
    {
        if (!$this->scopeConfig->isSetFlag(self::XML_INCLUDE_VIDEO, ScopeInterface::SCOPE_STORE, $storeId)) {
            return;
        }

        $store   = $this->storeManager->getStore($storeId);
        $baseUrl = rtrim((string) $store->getBaseUrl(), '/') . '/';

        $conn = $this->resource->getConnection();

        /** @var \PDO|null $pdo */
        $pdo = $conn->getConnection();

        $galleryTable      = $this->resource->getTableName('catalog_product_entity_media_gallery');
        $galleryValueTable = $this->resource->getTableName('catalog_product_entity_media_gallery_value');
        $videoTable        = $this->resource->getTableName('catalog_product_entity_media_gallery_value_video');
        $urlRewriteTable   = $this->resource->getTableName('url_rewrite');

        /*
         * Join media gallery -> gallery value -> video table -> url_rewrite to get
         * product URLs with their associated external video metadata in one pass.
         *
         * Filters:
         *  - media_type = 'external-video'
         *  - store-scoped gallery value (store_id = $storeId OR 0)
         *  - url_rewrite: autogenerated product URLs for the given store
         */
        $sql = sprintf(
            'SELECT DISTINCT
                ur.request_path,
                v.url      AS video_url,
                v.title    AS video_title,
                v.description AS video_description,
                v.thumbnail AS video_thumbnail,
                mgv.label  AS video_label
            FROM %s AS mg
            INNER JOIN %s AS mgv
                ON mgv.value_id = mg.value_id
                AND mgv.store_id IN (0, %d)
            INNER JOIN %s AS v
                ON v.value_id = mg.value_id
                AND v.store_id IN (0, %d)
            INNER JOIN %s AS mgvte
                ON mgvte.value_id = mg.value_id
            INNER JOIN %s AS ur
                ON ur.entity_id = mgvte.entity_id
                AND ur.entity_type = %s
                AND ur.store_id = %d
                AND ur.redirect_type = 0
                AND ur.is_autogenerated = 1
            WHERE mg.media_type = %s
                AND v.url IS NOT NULL
                AND v.url != %s',
            $conn->quoteIdentifier($galleryTable),
            $conn->quoteIdentifier($galleryValueTable),
            $storeId,
            $conn->quoteIdentifier($videoTable),
            $storeId,
            $conn->quoteIdentifier(
                $this->resource->getTableName('catalog_product_entity_media_gallery_value_to_entity')
            ),
            $conn->quoteIdentifier($urlRewriteTable),
            $conn->quote('product'),
            $storeId,
            $conn->quote('external-video'),
            $conn->quote('')
        );

        // Unbuffered query to avoid buffering large result sets.
        if ($pdo instanceof \PDO) {
            try {
                $pdo->setAttribute(\PDO::MYSQL_ATTR_USE_BUFFERED_QUERY, false);
            } catch (\Throwable) {
                // driver may not allow mid-connection toggle
            }
        }

        $stmt = $pdo instanceof \PDO ? $pdo->query($sql) : $conn->query($sql);
        if (!$stmt) {
            return;
        }

        try {
            // Group videos by request_path so one <url> entry can hold multiple videos
            $currentPath = null;
            $videos      = [];

            while ($row = $stmt->fetch(\PDO::FETCH_ASSOC)) {
                $path = (string) ($row['request_path'] ?? '');
                if ($path === '') {
                    continue;
                }

                if ($currentPath !== null && $currentPath !== $path) {
                    yield $this->buildEntry($baseUrl, $currentPath, $videos);
                    $videos = [];
                }

                $currentPath = $path;
                $videoUrl    = (string) ($row['video_url'] ?? '');
                if ($videoUrl === '') {
                    continue;
                }

                $videos[] = [
                    'content_loc'   => $videoUrl,
                    'title'         => (string) ($row['video_title'] ?? $row['video_label'] ?? ''),
                    'description'   => (string) ($row['video_description'] ?? ''),
                    'thumbnail_loc' => (string) ($row['video_thumbnail'] ?? ''),
                ];
            }

            // Emit the last group
            if ($currentPath !== null && $videos !== []) {
                yield $this->buildEntry($baseUrl, $currentPath, $videos);
            }
        } finally {
            if ($pdo instanceof \PDO) {
                try {
                    $pdo->setAttribute(\PDO::MYSQL_ATTR_USE_BUFFERED_QUERY, true);
                } catch (\Throwable) {
                    // ignore
                }
            }
        }
    }

    /**
     * @param array<int, array{content_loc: string, title: string, description: string, thumbnail_loc: string}> $videos
     * @return array{loc: string, changefreq: string, priority: float, video: array<int, array<string, string>>}
     */
    private function buildEntry(string $baseUrl, string $path, array $videos): array
    {
        return [
            'loc'        => $baseUrl . ltrim($path, '/'),
            'changefreq' => 'weekly',
            'priority'   => 0.8,
            'video'      => $videos,
        ];
    }
}
